package java_Day3_Global_LocalVariables;

public class SwappingwithiutThirdvariable {

	public static void main(String[] args) {
		
		int a=10;
		int b=20;
		
		System.out.println("Before swapping value of a :"+a);
		System.out.println("Before swapping value of b :"+b);
		
		
		a=30; //a=a+b=30
		b=10;  //b=a-b=20
		a=20;  //a=a-b=10
		
		System.out.println("After swapping of a:"+a);

		System.out.println("After swapping of b:"+b);
	
		
		
		
		
	}

}
