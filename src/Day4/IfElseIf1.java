package Day4;

public class IfElseIf1 {

	public static void main(String[] args) {
		
		// check number is positive , negative or Zero
		
		
		int number = 0;
		
		if(number>0)
		{
			System.out.println("positive");
			
		}
		else if(number<0)
			System.out.println("negative");
		
		else 
		{
			System.out.println("Zero");
		}
		
		
		
	}

}
