package JavaDay5_ForLoop;

public class ForLoop_PrnEvenOddNumber {

	public static void main(String[] args) {
		
		///even-2,4,6,8,10...
		for(int i=2; i<=20; i=i+2) 
		  {	
		    System.out.println(i);
		  }
		
		System.out.println("------------------------------");
		
		//even-1,3,5,7,9.....
		for(int i=1;i<=15;i=i+2) {
			
			System.out.println(i);
		}
		
	}

}
